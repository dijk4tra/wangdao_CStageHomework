#include <stdio.h>

// 冒泡排序函数：通过相邻元素比较和交换，将数组按升序排列
// arr[]：待排序的整数数组，n：数组元素个数
void bubbleSort(int arr[], int n) {
    int i, j, temp;  // 定义循环变量和临时交换变量
    // 外层循环：控制排序轮数，n个元素需要n-1轮比较
    // 每轮会将当前未排序部分的最大值"冒泡"到正确位置
    for (i = 0; i < n - 1; i++) {
        // 内层循环：执行相邻元素比较和交换
        // n-1-i：每轮结束后，最后i个元素已有序，无需再比较
        for (j = 0; j < n - 1 - i; j++) {
            // 如果前一个元素大于后一个元素，则交换它们（升序排序）
            if (arr[j] > arr[j + 1]) {
                // 交换两个元素的位置
                temp = arr[j];        // 临时存储前一个元素
                arr[j] = arr[j + 1];  // 将后一个元素前移
                arr[j + 1] = temp;    // 将临时存储的元素后移
            }
        }
        // 到这里时，第i轮结束，第n-1-i位置的元素已正确排序
    }
}

int main() {
    // 定义并初始化一个包含10个整数的测试数组
    int arr[10] = {8, 2, 5, 9, 1, 3, 7, 4, 6, 0};
    int n = 10;  // 数组长度
    // 输出排序前的数组
    printf("排序前的数组: ");
    for (int k = 0; k < n; k++) {
        printf("%d ", arr[k]);
    }
    printf("\n");
    // 调用冒泡排序函数对数组进行排序
    bubbleSort(arr, n);
    // 输出排序后的数组
    printf("排序后的数组: ");
    for (int k = 0; k < n; k++) {
        printf("%d ", arr[k]);
    }
    printf("\n");

    return 0;  // 程序正常结束
}
