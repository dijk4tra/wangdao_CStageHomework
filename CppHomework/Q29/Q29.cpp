#include <stdio.h>

// 计算一个16位无符号整数的四个4位半字节(nibble)的和
// 一个16位整数可以分成4个4位的半字节（nibble），每个半字节的值范围是0-15
// 函数依次提取每个半字节并求和
int sumOfNibbles(unsigned short num) {
    // unsigned short 通常是16位（2字节）整数，范围0-65535
    int sum = 0;
    // 提取第1个半字节（最低4位，位0-3）
    // 0xF的二进制是0000 0000 0000 1111
    // 与操作(num & 0xF)会保留num的最后4位，其他位清零
    sum += (num & 0xF);

    // 提取第2个半字节（位4-7）
    // 先右移4位，将原来的4-7位移动到0-3位
    // 再与0xF进行与操作，提取这4位的值
    sum += ((num >> 4) & 0xF);

    // 提取第3个半字节（位8-11）
    // 先右移8位，再与0xF提取
    sum += ((num >> 8) & 0xF);

    // 提取第4个半字节（最高4位，位12-15）
    // 先右移12位，再与0xF提取
    sum += ((num >> 12) & 0xF);

    return sum;  // 返回四个半字节的总和
}

int main() {
    // 示例数字：54711（十进制）
    // 二进制表示：1101 0101 1011 0111
    // 按4位一组拆分：1101(13) 0101(5) 1011(11) 0111(7)
    // 计算总和：13 + 5 + 11 + 7 = 36
    unsigned short num = 54711;

    // 输出原始数字
    printf("数字: %d\n", num);

    // 计算并输出四个半字节的和
    printf("4位拆分求和结果: %d\n", sumOfNibbles(num));

    return 0;
}