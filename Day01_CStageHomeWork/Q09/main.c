#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>

/*
编写一个C程序，找出所有的水仙花数并统计个数。

水仙花数是指一个三位数，其各位数字的立方和等于该数本身。例如：153 = 1³ + 5³ + 3³ = 1 + 125 + 27 = 153

要求：
1. 使用for循环遍历所有的三位数（100-999）
2. 分离出百位、十位和个位数字
3. 计算各位数字的立方和
4. 判断是否为水仙花数，如果是则输出
5. 统计水仙花数的个数并输出

考点： 嵌套循环，整数分解
*/

int main(void) {
    int count = 0;
    printf("以下是所有的水仙花数：\n");
    for (int n = 100; n <= 999; n++) {
        // 分离出百位、十位和个位数字
        int i = n / 100;         // 百位数字
        int j = (n / 10) % 10;   // 十位数字
        int k = n % 10;          // 个位数字
        // 计算各位数字的立方和
        if (n == (i * i * i + j * j * j + k * k * k)) {
            printf("%d\n", n);   // 如果是水仙花数，则输出
            count++;             // 统计水仙花数的个数
        }
    }

    printf("水仙花数的总个数为：%d个\n", count);
    return 0;
}