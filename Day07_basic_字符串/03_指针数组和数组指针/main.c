#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>

void test1() {
	// 什么是指针数组，什么是数组指针。 

	// 概念性的东西： 文字看后缀，后缀是什么，就是什么。 
	// 指针数组，是数组。 这个数组里面是装指针的。 
	// 数组指针，是指针。 这个指针是指向数组的。 +1偏移量不一样。 

	// 详细说明：
	// 指针数组（Array of Pointers）：它是一个数组，数组的每个元素都是指针。
	// 例如：int *arr[5];  // arr 是一个数组，包含5个 int* 类型的指针。
	// 数组指针（Pointer to Array）：它是一个指针，指向一个数组。
	// 例如：int (*ptr)[5]; // ptr 是一个指针，指向一个包含5个 int 元素的数组。
	// 关键区别：指针数组是“装着指针的数组”，数组指针是“指向数组的指针”。
	// 在内存中，指针数组占用多个指针大小的空间（每个元素是一个指针），
	// 而数组指针只占用一个指针大小的空间（它只存储一个地址）。
}

void test2() {

	int *p1[10];   // 指针数组：p1 是一个数组，包含10个 int *类型的元素
	int (*p2)[5];  // 数组指针：p2 是一个指针，指向一个包含5个 int 元素的数组

	// 平常，我们见到这两种写法，需要认出来，它是什么东西。 
	// [] 优先级高于 *，所以需要根据优先级判断 

	// int *p1[10];是什么呢？
	// [] 优先级高，所以 p1 先与 [10] 结合，说明 p1 是一个数组
	// 然后 int * 用来修饰数组的元素类型，说明这个数组中装的是 int* 类型的指针
	// 因此 p1 是一个指针数组

	// int (*p2)[5];
	// 小括号改变了优先级，(*p2) 表示 p2 是一个指针，
	// 然后与 [5] 结合，表示这个指针指向一个长度为5的 int 数组
	// 因此 p2 是一个数组指针

	// 注意：指针数组的大小由元素个数和指针大小决定（如 10 * sizeof(int*)），
	// 而数组指针的大小就是指针本身的大小（如 sizeof(int(*)[5]) = 8 字节在64位系统）
}

void test3() {
	// 实际有什么用？
	// 指针数组：是一个数组，数组里面装的一个一个的指针。

	int a = 10;
	int b = 20;
	int c = 30;
	int d = 40;
	int e = 50;

	// 想有一个数组，将这些内容的地址全部存下。
	// 可以使用指针数组。
	// 定义一个指针数组，包含6个 int* 元素，初始化前5个为变量地址，第6个默认为0（NULL）。
	int *p1[6] = { &a, &b, &c, &d, &e };
	
	// 通过指针数组访问变量的值：*p1[0] 等价于 *(p1[0])，即取出 p1[0] 中存储的地址，再解引用
	printf("%d\n", *p1[0]);  // 输出 a 的值 10
	printf("%d\n", *p1[1]);  // 输出 b 的值 20
	printf("%d\n", *p1[2]);  // 输出 c 的值 30
	printf("%d\n", *p1[3]);  // 输出 d 的值 40
	printf("%d\n", *p1[4]);  // 输出 e 的值 50
	
	// 修改指针数组元素所指向的变量
	*p1[0] = 888;			 // 将 p1[0] 指向的变量 a 的值改为 888
	printf("%d\n", *p1[0]);  // 输出 888
	printf("%d\n", a);       // a 的值已被修改，输出 888

	// 指针数组常用于存储多个字符串（字符指针数组）、动态分配的内存块地址等
}

