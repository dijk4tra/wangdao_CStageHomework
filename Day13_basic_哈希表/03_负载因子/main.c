#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>

/*
 * 哈希表 (Hash Table) 的存储原理与扩容机制
 * 结构：拉链法（数组 + 单链表）
 */
int main(void) {

    // 1. 存储结构：数组 + 链表
    // 数组（桶/Buckets）：哈希表的主体，通过哈希函数计算键（Key）的索引，实现常数级 O(1) 的访问速度。
    // 链表（冲突处理）：当不同的键计算出相同的索引时（哈希冲突），在该索引处以链表形式挂载数据。
    //     
    // 2. 核心矛盾：时间 vs 空间
    // 理论上，链表可以无限延长，存储任意数量的数据。
    // 缺陷：如果链表过长，哈希表会退化为普通的链表，查找效率从 O(1) 降至 O(n)，违背了哈希表“快速查找”的初衷。
    // 目标：通过合理的设计，既不浪费过多内存（空间），又能保持极快的查找速度（时间）。

    // 3. 关键指标：加载因子 (Load Factor)
    // 定义：加载因子 = 哈希表中已存储的键值对总数 / 数组的总长度。
    // 它是衡量哈希表“拥挤程度”或“稀疏程度”的量化指标。

    // 示例对比：
    // 情况 A：2/10 = 0.2（非常稀疏，空间利用率低，但几乎没有冲突，速度极快）
    // 情况 B：10/10 = 1.0（刚好填满，冲突概率开始显著增大）
    // 情况 C：200/10 = 20.0（极其拥挤，每个数组位置平均挂了 20 个节点，性能严重下降）

    // 4. 扩容机制 (Rehashing)
    // 规则：随着数据存入，加载因子持续变大。当它超过设定的“阈值”时，就需要进行“扩容”。
    // 操作：通常会创建一个容量翻倍的新数组，并将原有的所有数据重新计算哈希值，搬迁到新数组中。

    // 5. 工业标准：3/4 (0.75) 阈值
    // 经过大量数学演算和工业实践（如 Java 的 HashMap），0.75 被认为是一个完美的平衡点：
    // - 低于 0.75：空间浪费相对较多，但冲突极少。
    // - 高于 0.75：空间利用率高，但链表变长，碰撞概率以指数级上升。
    // 结论：当加载因子达到 0.75 时触发扩容，可以兼顾存储效率与查询性能。

    return 0;
}